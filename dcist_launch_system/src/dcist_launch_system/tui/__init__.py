@cli.command()
@click.option("--machine", "-m", multiple=True, help="Filter to specific machines")
@click.option("--detail", is_flag=True, help="Show subdirs and sizes")
@click.pass_context
def maps(ctx, machine, detail):
    """List experiments on fleet machines."""
    fc = FleetContext(
        ctx.obj["network"], ctx.obj["topology"], ctx.obj["output_root"]
    )
    names = list(machine) if machine else None
    machines = fc.machines(names=names)

    def fetch(m):
        return list_remote_experiments(m["user"], m["ip"], fc.output_root)

    results = run_parallel(fetch, machines)

    table = Table(title="Experiments")
    table.add_column("Machine", style="bold")
    table.add_column("Experiment")
    if detail:
        table.add_column("Subdirs")
        table.add_column("Size")

    for m, exps in sorted(results, key=lambda x: x[0]["name"]):
        if isinstance(exps, Exception):
            row = [m["name"], f"[red]Error: {exps}[/]"]
            if detail:
                row += ["", ""]
            table.add_row(*row)
            continue
        if not exps:
            row = [m["name"], "[dim]no experiments[/]"]
            if detail:
                row += ["", ""]
            table.add_row(*row)
            continue
        for i, exp in enumerate(sorted(exps, key=lambda e: e["name"])):
            mname = m["name"] if i == 0 else ""
            row = [mname, exp["name"]]
            if detail:
                row += [", ".join(exp["subdirs"]), exp["size"]]
            table.add_row(*row)

    console.print(table)


