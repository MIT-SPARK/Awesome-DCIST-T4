@cli.command()
@click.option("--role", type=click.Choice(["robot", "base_station"]), default=None)
@click.pass_context
def status(ctx, role):
    """Check connectivity to all fleet machines (ping + SSH)."""
    fc = FleetContext(
        ctx.obj["network"], ctx.obj["topology"], ctx.obj["output_root"]
    )
    roles = [role] if role else None
    machines = fc.machines(roles=roles)
    if not machines:
        click.secho("No machines found on this network.", fg="yellow")
        return

    click.secho(f"Network: {fc.network}  ({len(machines)} machines)\nChecking connectivity...", fg="cyan")
    reachable, unreachable = filter_reachable(machines)

    all_checked = reachable + unreachable
    all_checked.sort(key=lambda m: m["name"])

    table = Table(title="Fleet Status")
    table.add_column("Machine", style="bold")
    table.add_column("Role")
    table.add_column("Platform")
    table.add_column("IP")
    table.add_column("Ping")
    table.add_column("SSH")
    table.add_column("Status")

    for m in all_checked:
        ping_str = "[green]OK[/]" if m.get("ping") else "[red]FAIL[/]"
        ssh_str = "[green]OK[/]" if m.get("ssh") else "[red]FAIL[/]"
        if m.get("online"):
            status_str = "[bold green]ONLINE[/]"
        elif m.get("ping"):
            status_str = "[yellow]NO SSH[/]"
        else:
            status_str = "[red]OFFLINE[/]"
        table.add_row(
            m["name"], m["role"], m["platform_id"] or "---",
            m["ip"], ping_str, ssh_str, status_str,
        )

    console.print(table)
    n_online = len(reachable)
    n_total = len(all_checked)
    console.print(f"\n[bold]{n_online}/{n_total} machines online[/]")


