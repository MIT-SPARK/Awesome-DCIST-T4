@cli.command()
@click.option("--experiment", "-e", required=True, help="Experiment to verify")
@click.option("--machine", "-m", multiple=True, help="Filter to specific machines")
@click.pass_context
def verify(ctx, experiment, machine):
    """Verify map consistency across machines."""
    fc = FleetContext(
        ctx.obj["network"], ctx.obj["topology"], ctx.obj["output_root"]
    )
    names = list(machine) if machine else None
    machines = fc.machines(names=names)

    def fetch_hashes(m):
        return hash_remote_experiment(m["user"], m["ip"], fc.output_root, experiment)

    results = run_parallel(fetch_hashes, machines)
    machine_hashes = {m["name"]: h for m, h in results if isinstance(h, dict)}

    if not machine_hashes:
        click.secho("No machines returned hash data.", fg="yellow")
        return

    # Collect all files across all machines
    all_files = sorted(set(f for h in machine_hashes.values() for f in h))
    machine_names = sorted(machine_hashes.keys())

    table = Table(title=f"Map Verification: {experiment}")
    table.add_column("File", style="bold")
    for mname in machine_names:
        table.add_column(mname)

    diffs = 0
    for fpath in all_files:
        hashes_for_file = {mn: machine_hashes[mn].get(fpath) for mn in machine_names}
        unique_hashes = set(v for v in hashes_for_file.values() if v)
        is_consistent = len(unique_hashes) <= 1

        row = [fpath]
        for mn in machine_names:
            h = hashes_for_file[mn]
            if h is None:
                row.append("[dim]missing[/]")
                if unique_hashes:
                    is_consistent = False
            elif is_consistent:
                row.append("[green]OK[/]")
            else:
                row.append(f"[red]{h[:8]}[/]")
        if not is_consistent:
            diffs += 1
        table.add_row(*row)

    console.print(table)
    if diffs == 0:
        console.print("[bold green]All files consistent across machines.[/]")
    else:
        console.print(f"[bold red]{diffs} file(s) differ across machines.[/]")


