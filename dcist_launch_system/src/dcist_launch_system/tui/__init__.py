@cli.command()
@click.option("--machine", "-m", multiple=True, help="Filter to specific machines")
@click.option("--watch", "-w", is_flag=True, help="Auto-refresh every 10s")
@click.pass_context
def monitor(ctx, machine, watch):
    """Monitor fleet status (tmux sessions, ROS2 procs, load, disk)."""
    fc = FleetContext(
        ctx.obj["network"], ctx.obj["topology"], ctx.obj["output_root"]
    )
    names = list(machine) if machine else None
    machines = fc.machines(names=names)

    def _print_status():
        def fetch(m):
            return get_remote_status(m["user"], m["ip"])

        results = run_parallel(fetch, machines)

        table = Table(title="Fleet Monitor")
        table.add_column("Machine", style="bold")
        table.add_column("Tmux Sessions")
        table.add_column("ROS2 Procs")
        table.add_column("Load")
        table.add_column("Disk Free")

        for m, st in sorted(results, key=lambda x: x[0]["name"]):
            if isinstance(st, Exception):
                table.add_row(m["name"], f"[red]{st}[/]", "", "", "")
                continue
            table.add_row(
                m["name"],
                st["tmux_sessions"],
                st["ros2_procs"],
                st["load"],
                st["disk"],
            )
        console.print(table)

    if not watch:
        _print_status()
        return

    import time

    try:
        while True:
            console.clear()
            _print_status()
            console.print("[dim]Refreshing in 10s... (Ctrl+C to stop)[/]")
            time.sleep(10)
    except KeyboardInterrupt:
        pass


@cli.command()
@click.option("--machine", "-m", multiple=True, help="Filter to specific machines")
@click.option("--dry-run", is_flag=True, help="Preview configs without deploying")
@click.pass_context
def zenoh(ctx, machine, dry_run):
    """Generate and deploy Zenoh router configs based on topology.

    Base stations get connect endpoints to robots (star topology).
    Robots listen only — no outbound connections.
    """
    fc = FleetContext(
        ctx.obj["network"], ctx.obj["topology"], ctx.obj["output_root"]
    )
    names = list(machine) if machine else None
    machines = fc.machines(names=names)

    table = Table(title=f"Zenoh Configs — network: {fc.network}")
    table.add_column("Machine", style="bold")
    table.add_column("Role")
    table.add_column("Connect Endpoints")
    table.add_column("Status")

    for m in sorted(machines, key=lambda x: x["name"]):
        mname = m["name"]
        try:
            endpoints = generate_zenoh_endpoints(fc.topology, fc.network, mname)
        except KeyError:
            table.add_row(mname, m["role"], "?", "[red]No address[/]")
            continue

        n = len(endpoints)
        connect_str = f"{n} robot(s)" if n else "[dim]none (listen only)[/]"

        if dry_run:
            table.add_row(mname, m["role"], connect_str, "[yellow]dry-run[/]")
            for ep in endpoints:
                console.print(f"  [dim]{mname} → {ep}[/]")
        else:
            ok, msg = deploy_zenoh_config(m["user"], m["ip"], endpoints)
            status = f"[green]{msg}[/]" if ok else f"[red]{msg}[/]"
            table.add_row(mname, m["role"], connect_str, status)

    console.print(table)
    if dry_run:
        console.print("[yellow]Dry run — no configs deployed. Remove --dry-run to deploy.[/]")


