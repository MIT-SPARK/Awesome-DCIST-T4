@cli.command()
@click.option("--machine", "-m", multiple=True, help="Machines to launch on")
@click.option("--session", "-s", required=True, help="Tmux session name")
@click.option("--experiment", "-e", required=True, help="Experiment name (for output dir)")
@click.option("--prior-map", "-p", default=None, help="Prior map path on remote machines")
@click.option("--base-station", is_flag=True, help="Also launch base station on willow")
@click.option("--force", "-f", is_flag=True, help="Overwrite existing output directories")
@click.option("--yes", "-y", is_flag=True, help="Skip confirmation")
@click.option("--dry-run", "-d", is_flag=True, help="Show commands without executing")
@click.pass_context
def launch(ctx, machine, session, experiment, prior_map, base_station, force, yes, dry_run):
    """Launch run-adt4 remotely on fleet robots."""
    fc = FleetContext(
        ctx.obj["network"], ctx.obj["topology"], ctx.obj["output_root"]
    )
    names = list(machine) if machine else None
    robots = fc.machines(roles=["robot"], names=names)

    targets = []
    for m in robots:
        sess = session
        targets.append((m, sess))

    if base_station:
        bs_machines = fc.machines(roles=["base_station"])
        for m in bs_machines:
            targets.append((m, "base_station"))

    if not targets:
        click.secho("No target machines found.", fg="yellow")
        return

    table = Table(title="Launch Plan")
    table.add_column("Machine", style="bold")
    table.add_column("Session")
    table.add_column("Platform")
    table.add_column("Output Dir")
    table.add_column("Prior Map")

    cmds = []
    force_flag = " -f" if force else ""
    for m, sess in targets:
        output_dir = f"{fc.output_root}/{experiment}"
        prior_flag = f" -p {_quote_path(prior_map)}" if prior_map else ""
        remote_cmd = (
            f"cd ~/dcist_ws && source install/setup.zsh && "
            f"src/awesome_dcist_t4/dcist_launch_system/bin/run-adt4 "
            f"{shlex.quote(sess)} -n {shlex.quote(m['name'])} "
            f"-c {shlex.quote(m['platform_id'])} "
            f"-o {_quote_path(output_dir)} -y{force_flag}{prior_flag} "
            f"--tmuxp-args \"-d\""
        )
        table.add_row(
            m["name"], sess, m["platform_id"] or "---",
            output_dir, prior_map or "---",
        )
        cmds.append((m, remote_cmd, sess))

    console.print(table)

    if dry_run:
        click.secho("\nDry run — commands that would be executed:", fg="cyan")
        for m, cmd, _ in cmds:
            click.echo(f"  ssh {m['user']}@{m['ip']} \"{cmd}\"")
        return

    # Pre-launch check for existing sessions
    click.echo("Checking for running sessions...")
    def check_session(item):
        m, _, sess = item
        rc, _, _ = ssh_cmd(m["user"], m["ip"], f"tmux has-session -t {shlex.quote(sess)} 2>/dev/null", timeout=5)
        return m['name'], rc == 0, sess

    session_checks = run_parallel(check_session, cmds)
    conflicts = [r for _, r in session_checks if r[1]]
    if conflicts:
        for mname, _, sess in conflicts:
            click.secho(f"  Warning: Session '{sess}' is already running on {mname}.", fg="yellow")
        if click.confirm("Terminate existing sessions and proceed?"):
            for mname, _, sess in conflicts:
                # Find the machine dict
                m = next(m for m, _, _ in cmds if m["name"] == mname)
                click.echo(f"  Terminating session on {mname}...")
                ssh_cmd(m["user"], m["ip"], f"tmux kill-session -t {shlex.quote(sess)}", timeout=5)
        else:
            click.secho("Aborted by user.", fg="red")
            return

    if not yes:
        click.confirm("Proceed with launch?", abort=True)

    def do_launch(item):
        m, cmd, _ = item
        rc, out, err = ssh_cmd(m["user"], m["ip"], cmd, timeout=30)
        return rc, out, err

    results = run_parallel(do_launch, cmds)
    for (m, _), (rc_or_exc) in results:
        if isinstance(rc_or_exc, Exception):
            click.secho(f"  {m['name']}: ERROR - {rc_or_exc}", fg="red")
        else:
            rc, out, err = rc_or_exc
            if rc == 0:
                click.secho(f"  {m['name']}: launched", fg="green")
            else:
                click.secho(f"  {m['name']}: FAILED (rc={rc}) {err}", fg="red")


