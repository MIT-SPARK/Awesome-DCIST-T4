@cli.command()
@click.option("--from", "src_machine", required=True, help="Source machine name")
@click.option("--experiment", "-e", required=True, help="Experiment directory name")
@click.option("--as", "dest_name", default=None, help="Rename experiment on destination")
@click.option("--to", "dst_machines", multiple=True, help="Dest machines (default: all other robots)")
@click.option("--subdir", default=None, help="Only transfer a specific subdir (e.g. hydra, roman)")
@click.option("--include-bags", is_flag=True, help="Include recorded_data/ (rosbags). Excluded by default.")
@click.option("--delete", is_flag=True, help="Delete extraneous files on destination")
@click.option("--relay", is_flag=True, help="Force relay through operator machine")
@click.pass_context
def transfer(ctx, src_machine, experiment, dest_name, dst_machines, subdir, include_bags, delete, relay):
    """Transfer an experiment from one machine to others. Excludes rosbags by default."""
    fc = FleetContext(
        ctx.obj["network"], ctx.obj["topology"], ctx.obj["output_root"]
    )
    all_machines = fc.machines()
    src = next((m for m in all_machines if m["name"] == src_machine), None)
    if not src:
        click.secho(f"Source machine '{src_machine}' not found on network.", fg="red")
        return

    src_status = check_machine(src)
    if not src_status["online"]:
        click.secho(f"Source machine '{src_machine}' ({src['ip']}) is not reachable (ping={src_status['ping']}, ssh={src_status['ssh']}).", fg="red")
        return

    if dst_machines:
        dests = [m for m in all_machines if m["name"] in dst_machines]
    else:
        dests = [m for m in all_machines if m["name"] != src_machine]

    if not dests:
        click.secho("No destination machines found.", fg="yellow")
        return

    click.echo("Checking destination reachability (ping + SSH)...")
    reachable, unreachable = filter_reachable(dests)
    for dst in unreachable:
        reason = "offline" if not dst.get("ping") else "no SSH"
        click.secho(f"  Skipping {dst['name']} ({dst['ip']}) — {reason}", fg="yellow")
    if not reachable:
        click.secho("No reachable destinations.", fg="red")
        return

    dest_name = dest_name if dest_name is not None else f"prior_{experiment}"
    path_suffix = f"/{subdir}" if subdir else ""
    src_path = f"{fc.output_root}/{experiment}{path_suffix}"

    label = f"{src_machine}:{src_path}"
    if dest_name != experiment:
        label += f" (as {dest_name})"
    exclude = [] if include_bags else ["recorded_data/"]
    if not include_bags:
        click.secho("Excluding rosbags (recorded_data/). Use --include-bags to include.", fg="yellow")
    click.secho(f"Transferring {label} -> {len(reachable)} machines", fg="cyan")
    for dst in reachable:
        dst_path = f"{fc.output_root}/{dest_name}{path_suffix}"
        click.echo(f"  -> {dst['name']} ({dst['ip']})...")
        ok, method, msg = rsync_transfer(
            src["user"], src["ip"], src_path,
            dst["user"], dst["ip"], dst_path,
            delete=delete, relay=relay, stream=True, exclude=exclude,
        )
        if ok:
            click.secho(f"  {dst['name']}: OK ({method})", fg="green")
        else:
            click.secho(f"  {dst['name']}: FAIL ({method}): {msg}", fg="red")


